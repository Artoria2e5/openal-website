		<h1>Alternative Implementations</h1>
		<p><strong><a href="http://www.openal-soft.org/">OpenAL Soft</a></strong> - This library is a compatible update/replacement to the deprecated OpenAL Sample Implementation (the SI). The original idea was to fork the old Windows version to attempt an accelerated ALSA version. The accelerated ALSA idea quickly fell through, but the software mixing code ended up being modified to be cross-platform, with multiple output backends: ALSA, OSS, DirectSound, PulseAudio, MMDevAPI, CoreAudio, Solaris, SoundIO, OpenSL, WinMM, PortAudio, "Null" Output, and a .wav writer are currently implemented.</p>

<p>OpenAL Soft's code has been improved to support mono, stereo, 4-channel, 5.1, 6.1, 7.1, and HRTF output. OpenAL Soft does not support the Vorbis and MP3 extensions however, these are considered deprecated. It does, though, support some of the newer extensions like AL_EXT_FLOAT32 and AL_EXT_MCFORMATS for multi-channel and floating-point formats, as well as ALC_EXT_EFX for environmental audio effects, and others.</p>
